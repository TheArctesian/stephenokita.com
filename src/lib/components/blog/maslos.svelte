  <div class="el el8 eln">
    <h1 class="font-bold">Transcendence Needs</h1>
    <p>
      Added by Maslow later in his life, transcendence involves moving beyond
      individual needs to connect with something greater than oneself. This can
      include helping others, having spiritual experiences, serving a higher
      purpose, and feeling a sense of unity with others and the world.
    </p>
  </div>
  <div class="el el7">
    <h1 class="font-bold">Self-Actualisation Needs</h1>
    <p>
      Self-actualisation refers to the desire for personal growth,
      self-fulfilment, and realising one's full potential. Yes, it is pretty
      poorly defined, I think of it as eudaimonia.
    </p>
  </div>
  <div class="el el6 eln">
    <h1 class="font-bold">Aesthetic Needs</h1>
    <p>
      The need to appreciate beauty, balance, form, and creativity. This can
      include the desire to create or experience art, music, dance, or other
      forms of creative expression. Aesthetics bring a sense of pleasure and
      fulfilment.
    </p>
  </div>
  <div class="el el5 eln">
    <h1 class="font-bold">Cognitive Needs</h1>
    <p>
      The need for knowledge, meaning, and self-awareness. People have a natural
      curiosity to learn, explore, and understand the world around them and make
      sense of their experiences and find meaning in their lives.
    </p>
  </div>
  <div class="el el4">
    <h1 class="font-bold">Esteem Needs</h1>
    <p>
      This category involves the need for self-respect, self-esteem, and the
      esteem of others, including recognition, status, and prestige.
    </p>
  </div>
  <div class="el el3">
    <h1 class="font-bold">Love and Belonging Needs</h1>
    <p>
      These include the desire for interpersonal relationships, friendship,
      intimacy, and a sense of connection with others.
    </p>
  </div>
  <div class="el el2">
    <h1 class="font-bold">Safety Needs:</h1>
    <p>
      This level involves the need for personal security, stability, and
      protection from harm. In essence the basic needs of a human being to live
      in a society beyond the necessities of survival (job, security (no one
      trying to kill you))
    </p>
  </div>
  <div class="el">
    <h1 class="font-bold">Physiological needs</h1>
    <p>
      These include the most basic requirements for survival, such as food,
      water, sleep, and shelter. He originally includes reproduction in here but
      that is slightly contentious and can be debated whether it belongs to a
      higher level.
    </p>
  </div>
